//-----------------------------------------------------------------------------
// FONCTION    :  HAL_Get_Nb_GPIO
//
// DESCRIPTION :
//-----------------------------------------------------------------------------
void HAL_CLOCK_Config(uint32_t ulRequest,bool enable)
{
    __HAL_RCC_GPIOA_CLK_ENABLE();

    switch ( ulRequest )
    {
        case ( SYSCFG_CLK ):
        {
            if ( enable )
                __HAL_RCC_SYSCFG_CLK_ENABLE();
            else
                __HAL_RCC_SYSCFG_CLK_DISABLE();
            break;
        }
        default:
        {
            break;
        }
    }
}
